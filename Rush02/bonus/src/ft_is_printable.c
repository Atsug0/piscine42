#include"../include/ft.h"

char	*ft_is_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] < 32 || str[i] >= 126)
			return (NULL);
		i++;
	}
	return (str);
}
