#include"../include/ft.h"

t_int	ft_atoi(char *str)
{
	t_int	num;

	num = 0;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	while (*str >= '0' && *str <= '9' && *str)
	{
		num = num * 10 + *str - 48;
		str++;
	}
	return (num);
}
