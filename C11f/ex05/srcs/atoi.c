int	ft_atoi(char *c)
{
	int	num;
	int	neg;
	int	i;

	num = 0;
	neg = 1;
	i = 0;
	while (c[i] && c[i] <= 32)
		i++;
	while (c[i] && (c[i] == '+' || c[i] == '-'))
	{
		if (c[i] == '-')
			neg *= -1;
		i++;
	}
	while (c[i] && c[i] <= '9' && c[i] >= '0')
	{
		num = num * 10 + c[i] - '0';
		i++;
	}
	return (neg * num);
}
